package ocb.capitoloPrimo.tipiDiPrimitivi;

public class Primitivi {

	//tipo= true o false
 	boolean b=false; //di default false (true/false)
 	//tipo= 8 bit int value
 	byte by=123; //da -128 a 127
 	//tipo= 16 bit int value
 	short s=123; //da -32,768 a 32,767
 	//tipo= 32 bit int value
 	int in=1223; //da -2BI a 2BI
 	//tipo= 64 bit int value
 	long ln=123L; // da -2(elevato alla63) a 2(elevato alla63)-1; valore di default=0L
 	//tipo= 32 bit floating-point value
 	float flt=123.45f; // n/a;  valore di default=0f 
 	//tipo= 64 bit floating-point value
 	double d=123.45;// n/a  valore di default=0.0
 	//tipo= 16 bit Unicode value
 	char ch='a';// da 0 a 65,535;  valore di default=\u0000
			
	
	
	
}
